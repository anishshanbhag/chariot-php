							<p class="text_align"><strong>Storage</strong></p>
							<p class="text_align">Wheat grains are scientifically stored at warehouse and periodically fumigated to avoid infestation and preserve the natural qualities of grain. Wheat is stored at ambient temperature and away from direct sunlight and water.</p>
							<p class="text_align"><strong>Cleaning </strong></p>
							<p class="text_align">Cleaning involves the separation of grain from other seeds & removal of materials like stones, dirt and chaffs accumulated during harvesting. Cleaning is done based on two principles, The average weight of the grain & the average size of the grain.</p>
							<p class="text_align"><strong>Conditioning</strong></p>
							<p class="text_align">The cleaned wheat is ready to be conditioned for milling. The process is called Tempering, where the right amount of moisture is added to solidify the bran and tender the inner endosperm. Wheat is kept in the conditioning bins for almost 30 hours before it is milled.</p>
							<p class="text_align"><strong>Gristing</strong></p>
							<p class="text_align">Gristing means separating the chaff from the grain. This is the preliminary step in cleaning the wheat.</p>
							<p class="text_align"><strong>Milling</strong></p>
							<p class="text_align">Grains are passed through the grinding rolls to break them individually and then milled into flour in the stone mill. Wheat is reduced to flour in a repetitive process.</p>
							<p class="text_align"><strong>Packaging</strong></p>
							<p class="text_align">Flour is packed in Automatic packing machines that weigh the flour accurately and discharge it in laminate pouches and HDPE bag. The bag is then sealed with a stitching machine.</p>
							<p class="text_align"><strong>Whole wheat chakki Atta Milling</strong></p>
							<p class="text_align">Whole wheat Chakki Atta Milling is also known as Stone Milling. The stone mill is one of the original mills used for grinding wheat which uses a combination of compression, shear, and abrasion. In whole wheat chakki atta milling, the final product is 96 % Atta and 4 % Bran and therefore, the product is more nutritious and tastier.</p>
							<p class="text_align"><strong>1. STORAGE:</strong> We store wheat grains by maintaining the right amount of moisture, heat and air, which preserves the freshness and nutritive value.</p>
							<p class="text_align"><strong>2. CLEANING: </strong>Cleaning involves the separation of grain from seeds and removal of other materials like stones, dirt and other seeds accumulated during wheat harvesting.</p>
							<p class="text_align"><strong>3. CONDITIONING: </strong>The cleaned wheat is ready to be conditioned for milling. The process is called Tempering, where the moisture is added to solidify the bran and lender the inner endosperm.</p>
							<p class="text_align"><strong>4. GRISTING: </strong>Gristing involves the blending of several classes of wheat until we obtain the desired quality of wheat flour.</p>
							<p class="text_align"><strong>5. CHAKKI MILLING: </strong>Cleaned and tempered grains are ground between a stationary and a moving stone, discharging the resultant flour by the rotation motion and conveying out by the grooves and furrows in the stone.</p>
							<p class="text_align"><strong>6. PACKAGING:</strong> The final step is for the flour to pass into Fill, Form and Seal machine, and to be stored for freshness and goodness. The Product is packed in 1Kg and 5Kg laminate pouches and 20 Kg HDPE bags.</p>
